#05_Comparison Operators.py
a = 5
b = 10
c = "5"
print("a == b มีผลลัพธ์เป็น ", a == b)
print("a == c มีผลลัพธ์เป็น ", a == c)  
print("a != b มีผลลัพธ์เป็น ", a != b)
print("a != c มีผลลัพธ์เป็น ", a != c) 
print("a > b มีผลลัพธ์เป็น ", a > b)  
print("a < b มีผลลัพธ์เป็น ", a < b)   
print("a >= b มีผลลัพธ์เป็น ", a >= b)  
print("a <= b มีผลลัพธ์เป็น ", a <= b)
