# สมมติว่าเรามีสวิตช์ไฟสี่ดวง (แสดงด้วยบิต) และเราต้องการเปิดไฟดวงที่สองและดวงที่สี่
light_status = 0b1010  # แสดงสถานะของไฟ (ดวงที่สองและดวงที่สี่เปิด)
light_check = 0b1111    # เราต้องการตรวจสอบไฟดวงที่สองและดวงที่สี่
result = light_status & light_check
print("สถานะของหลอดไฟ: ", bin(result))  # ผลลัพธ์: 0b1010 (ไฟดวงที่ 2 และ 4 ติดอยู่)
